    private void cmbDaftarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_cmbDaftarActionPerformed
        String idPeserta = txtId.getText();
        String nama = txtNama.getText();
        String prov = txtProvinsi.getText();
        String kab = txtKab.getText();
        String kec = txtKec.getText();
        String alamat = txtAlmt.getText();
        String email = txtEmail.getText();
        String telp = txtTelp.getText();
        double ipk = Double.parseDouble(txtIpk.getText());
        try {
            if (edit == true) {
                stm.executeUpdate("update peserta set nama='" + nama + "',prov='" + prov
                        + "',kab='" + kab + "',kec='" + kec + "',alamat='" + alamat + "',email='" + email + "',telp='"
                        + telp + "',ipk=" + ipk + ",skck='" + skck + "' where idPeserta='" + idPeserta + "'");
            } else {
                stm.executeUpdate("insert into peserta values('" + idPeserta + "' , '" + nama + "', '" + prov
                        + "', '" + kab + "', '" + kec + "', '" + alamat + "', '" + email + "','" + telp + "'," + ipk
                        + ", '" + skck + "')  ");
            }
            tblPeserta.setModel(new DefaultTableModel(dataTable, header));
            readData();
            aktif(false);
            setTombol(true);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan");
        }

    }

