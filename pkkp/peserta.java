    private void readData() {
        try {
            stm = Con.createStatement();
            rsPeserta = stm.executeQuery("select * from peserta");
            ResultSetMetaData meta = rsPeserta.getMetaData();
            int col = meta.getColumnCount();
            int baris = 0;
            while (rsPeserta.next()) {
                baris = rsPeserta.getRow();
            }
            dataTable = new Object[baris][col];
            int x = 0;
            rsPeserta.beforeFirst();
            while (rsPeserta.next()) {
                dataTable[x][0] = rsPeserta.getString("idPeserta");
                dataTable[x][1] = rsPeserta.getString("nama");
                dataTable[x][2] = rsPeserta.getString("prov");
                dataTable[x][3] = rsPeserta.getString("kab");
                dataTable[x][4] = rsPeserta.getString("kec");
                dataTable[x][5] = rsPeserta.getString("alamat");
                dataTable[x][6] = rsPeserta.getString("email");
                dataTable[x][7] = rsPeserta.getString("telp");
                dataTable[x][8] = rsPeserta.getString("ipk");
                dataTable[x][9] = rsPeserta.getString("skck");
                x++;
            }
            tblPeserta.setModel(new DefaultTableModel(dataTable, header));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

